#include "libft.h"

void	ft_lstiter(t_list	*lst, void (*f)(void *))
{
	t_list	*iter;

	if (lst && f)
	{
		iter = lst;
		while (iter)
		{
			f(iter->content);
			iter = iter->next;
		}
	}
	return ;
}
