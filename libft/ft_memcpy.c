#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*dest;
	char	*source;
	size_t	i;

	dest = (char *)dst;
	source = (char *)src;
	i = -1;
	if (n == 0 || dest == source)
		return (dest);
	while (++i < n)
		dest[i] = source[i];
	return ((void *)dest);
}
